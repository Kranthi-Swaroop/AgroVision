def get_translation(lang: str, key: str, fallback: str = None):
    """Get a translation for a specific key"""
    keys = key.split(".")
    result = TRANSLATIONS.get(lang, TRANSLATIONS["en"])
    
    for k in keys:
        if isinstance(result, dict):
            result = result.get(k)
        else:
            break
    
    if result is None:
        # Fallback to English
        result = TRANSLATIONS["en"]
        for k in keys:
            if isinstance(result, dict):
                result = result.get(k)
            else:
                break
    
    return result or fallback or key


def get_disease_name(disease_class: str, lang: str = "en") -> str:
    """Get translated disease name"""
    return get_translation(lang, f"diseases.{disease_class}", disease_class.replace("_", " ").title())


def get_risk_level_name(level: str, lang: str = "en") -> str:
    """Get translated risk level"""
    return get_translation(lang, f"risk_levels.{level}", level)


def get_severity_name(severity: str, lang: str = "en") -> str:
    """Get translated severity"""
    return get_translation(lang, f"severity.{severity}", severity)


