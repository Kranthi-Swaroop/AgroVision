// Get translation helper
export const getTranslation = (lang, path) => {
  const keys = path.split('.');
  let result = translations[lang] || translations.en;
  
  for (const key of keys) {
    result = result?.[key];
    if (!result) {
      // Fallback to English
      result = translations.en;
      for (const k of keys) {
        result = result?.[k];
      }
      break;
    }
  }
  
  return result || path;
};
